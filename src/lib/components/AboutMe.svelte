<section id="about-me" class="mt-8 md:mt-32 max-w-screen-2xl mx-auto px-4">
	<h1 class="text-5xl font-bold max-w-screen-2xl mx-auto">About Me.</h1>
	<p class="mt-8 text-xl">
		My interest in videos started when I was 3 years old; my dad had a JVC camcorder and filmed all
		of our family memories and holidays on there. I was very fascinated by the camera's features and
		what it could do, along with how the video turned out. In 2011, when I was 8 years old, I got my
		first computer that wasn't my parents computer: a tiny eMachines netbook with the legendary
		Windows 7 pre-installed. My twin sister and I used to make our own takes of the news, the Voice,
		and game shows, along with their own ads in my room, on the netbook's webcam, and put it all
		together on Windows Movie Maker and added all sorts of crazy video effects on there to make it
		funny.
	</p>
	<p class="mt-4 text-xl">
		We used to have heaps of fun making these skits and we used to love showing it off to our mum
		and dad. A few years later, every time I had my primary school friends over, we used to grab the
		iPad and make all sorts of different short films, and put them all together on iMovie. We used
		to think we were being so professional and that we were going to win Oscars for these films! I
		started making video montages when I was in Year 7, in 2016, when we had to do a small school
		project about well known figures and celebrities who have made an impact. I decided to do a
		project about Steve Jobs, the founder of Apple, and how he changed the technology world to how
		it is today.
	</p>
	<p class="mt-4 text-xl">
		I was struggling heaps with how I was going to present this project. Luckily, my mate Jono came
		to the rescue; he showed me a video edit of Steve Jobs' best moments that he made for a high
		school project, and I was very inspired, so I decided to make a video montage on iMovie on my
		MacBook Pro of Steve Jobs introducing products to the stage, from the very first Macintosh in
		1984, to the first iPhone in 2007. The video was a huge success, not only in the classroom, but
		even to the parents and staff. I then was assigned to do another project a few months later, and
		I decided to get back into the montage game again, and made an edit of my life, from when I was
		born, to the age of 13.
	</p>
	<p class="mt-4 text-xl">
		The video was again, a huge hit. It made people smile, laugh and cry. Fast forwarding to the
		present day, I have made videos on all sorts of things, such as family holidays, birthdays,
		weddings, tech reviews, car videos, and almost everything you can think of, I use more powerful
		computers and professional editing programs such as Adobe Premiere Pro to DaVinci Resolve Studio
		to make my videos, my editing styles have evolved over time to become more professional, casual,
		cinematic to trying all sorts of styles that fit with the context of the video. Filmmaking and
		video editing has become a lifelong interest and hobby that I really enjoy, and something that I
		will cherish and express my knowledge and skills for years to come.
	</p>
</section>
